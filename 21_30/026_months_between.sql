-- 026 날짜 간 개월 수 출력 (MONTHS_BETWEEN)
-- MONTHS_BETWEEN(최신 날짜, 예전 날짜)
-- 이름을 출력하고 입사한 날짜부터 오늘까지 총 몇 달을 근무했는지 출력 
SELECT ename, MONTHS_BETWEEN(sysdate, hiredate)
    FROM emp;

